package com.infomanagers.app.Model;

public class Barangay {
    private String address;
    private String credentialsSRC;
    private String date_of_creation;
    public String getAddress() {
        return address;
    }
    public void setAddress(String address) {
        this.address = address;
    }
    public String getCredentialsSRC() {
        return credentialsSRC;
    }
    public void setCredentialsSRC(String credentialsSRC) {
        this.credentialsSRC = credentialsSRC;
    }
    public String getDate_of_creation() {
        return date_of_creation;
    }
    public void setDate_of_creation(String date_of_creation) {
        this.date_of_creation = date_of_creation;
    }
}
